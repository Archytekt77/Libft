#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*ptr;
	int				i;

	ptr = s;
	i = 0;
	while (n--)
	{
		ptr[i] = c;
		i++;
	}
	return (s);
}
